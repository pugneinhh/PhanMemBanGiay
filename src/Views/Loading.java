    private void load(){
        new Thread(new Runnable() {
            @Override
            public void run() {
                for (int i = 0; i <= 100 ; i++) {
                    pgrload.setValue(i);
                    lblLoad.setText("Loading "+i +"%");
                    try {
                        Thread.sleep(60);
                    } catch (InterruptedException ex) {
                        Logger.getLogger(Loading.class.getName()).log(Level.SEVERE, null, ex);
                    }
                    if(i==100){
                        close();
                    }
                }
                
            }
        }).start();
    }
    private void close(){
        new FormNhanVien().setVisible(true);
    }
