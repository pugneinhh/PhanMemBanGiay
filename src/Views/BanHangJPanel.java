    private void loadTableBanHang() {
        ArrayList<ChiTietSanPhamModel> list = CTSPService.getAllChiTietSanPham();
        modelTable.setRowCount(0);
        for (ChiTietSanPhamModel ctspM : list) {
            modelTable.addRow(new Object[]{
                ctspM.getIdSP(),
                ctspM.getGiaBan(),
                ctspM.getSoLuong(),
                ctspM.getIdSize(),
                ctspM.getIdDC(),
                ctspM.getIdCL(),
                ctspM.getIdMS(),
                ctspM.getIdDM(),
                ctspM.getMota()
            });
        }
    }

